#This is to add a fruit 
fruits = ["apple","banana","cherry"]
fruits.append("kiwi")
print(fruits)

#This is to add a fruit in the second position
fruits.insert(1,"orange")
print(fruits)

#This is to remove a fruit
fruits.remove("kiwi")
print(fruits) 

#This is to sort the fruits in reverse alphabetical order
fruits.sort(reverse=True)
print(fruits)